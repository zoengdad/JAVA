public class ArithmeticOPTest3 {
	public static void main(String aa[])
	{
		int a = 10;
		System.out.println("(++a + ++a)의 결과는 : " + (++a + ++a));
		a = 10;
		System.out.println("(++a - ++a)의 결과는 : " + (++a - ++a));
		a = 10;
		System.out.println("(a++ + a++)의 결과는 : " + (a++ + a++));
		a = 10;
		System.out.println("(a++ - a++)의 결과는 : " + (a++ - a++));
		a = 10;
		System.out.println("(++a + a++)의 결과는 : " + (++a + a++));
		a = 10;
		System.out.println("(++a - a++)의 결과는 : " + (++a - a++));
		a = 10;
		System.out.println("(a++ + ++a)의 결과는 : " + (a++ + ++a));
		a = 10;
		System.out.println("(a++ - ++a)의 결과는 : " + (a++ - ++a));
	}	
}